public class Ejer4_1 {
    public static void main(String[] args) {

        System.out.println("Te voy a mostrar una secuencia de numeros de 1 a 20 utilizando el metodo for");
        int i=1;
        for (i=1; i<=20; i++){
            System.out.println(i);
        }
        System.out.println("Te voy a mostrar una secuencia de numeros de 1 a 20 utilizando el metodo while");
        i=1;
        while (i<=20){
            System.out.println(i);
            i++;
        }
    }
}
